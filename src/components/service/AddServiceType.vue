<template>
  <div>
    <h1>Add Service Type</h1>
    <div class="field">
      <label class="label">Service Type</label>
      <div class="control">
        <input
          class="input"
          type="text"
          placeholder="Service Type Name"
          v-model="ServiceTypeName"
        />
      </div>
    </div>
    <div class="control">
      <button class="button is-success" @click="saveServiceType">Add</button>
      <router-link :to="{name:'ServiceTypeList'}"><button class="button is-danger">Cancel</button></router-link>
    </div>
  
  </div>
  

    
</template>
<script>
// import axios
import axios from "axios";


export default {
  name: "AddServiceType",
 
  data() {
    
    return {
        ServiceTypeName : ""
    };
  },
  
  methods: {
    // Create New ServiceType
    async saveServiceType() {
      try { 
        await axios.post("https://api-2np.herokuapp.com/ServiceType", {
            name: this.ServiceTypeName
        });
        this.ServiceTypeName = "";
        
        this.$router.push("/page/service-type");
      } catch (err) {
        console.log(err);
      }
    },
  },
};
</script>


